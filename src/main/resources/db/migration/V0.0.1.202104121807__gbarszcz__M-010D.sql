CREATE TABLE `TRAINING_CENTER`.`TRAINING_PLANS_TEMPLATES` (
  `TPT_ID` INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT 'Training plan template ID',
  `TPT_AUDIT_CD` DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL COMMENT 'Training plan creation date',
  `TPT_AUDIT_CU` INT UNSIGNED COMMENT 'Training plan creation user',
  `TPT_AUDIT_MD` DATETIME COMMENT 'Training plan modification date',
  `TPT_AUDIT_MU` INT UNSIGNED COMMENT 'Training plan modification user',
  `TPT_AUDIT_RD` DATETIME COMMENT 'Training plan removal date',
  `TPT_AUDIT_RU` INT UNSIGNED COMMENT 'Training plan removal user',
  `TPT_NAME` VARCHAR(45) DEFAULT NULL COMMENT 'Training plan template name',
  `ACC_ID` INT NOT NULL COMMENT 'Training plan owner ID',
  PRIMARY KEY (`TPT_ID`),
  INDEX `ACCOUNT_FK_IDX` (`ACC_ID` ASC),
  CONSTRAINT `TPT_ACCOUNT_FK`
    FOREIGN KEY (`ACC_ID`)
    REFERENCES `ACCOUNTS` (`ACC_ID`)
);

