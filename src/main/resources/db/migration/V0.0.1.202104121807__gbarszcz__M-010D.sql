  `TSE_AUDIT_CU` INT UNSIGNED COMMENT 'Training series creation user',
  `TSE_AUDIT_MD` DATETIME COMMENT 'Training series modification date',
  `TSE_AUDIT_MU` INT UNSIGNED COMMENT 'Training series modification user',
  `TSE_AUDIT_RD` DATETIME COMMENT 'Training series removal date',
  `TSE_AUDIT_RU` INT UNSIGNED COMMENT 'Training series removal user',
  `TSE_THI_ID` INT UNSIGNED DEFAULT NULL COMMENT 'Training ID',
  `TSE_EXC_ID` INT UNSIGNED DEFAULT NULL COMMENT 'Exercise ID',
  `TSE_UNIT` INT UNSIGNED DEFAULT NULL COMMENT 'Training unit',
  `TSE_TSR_ID` INT UNSIGNED DEFAULT NULL COMMENT 'Training series result ID',
  PRIMARY KEY (`TSE_ID`),
  INDEX `TSE_THI_FK_IDX` (`TSE_THI_ID` ASC),
  INDEX `TSE_EXC_FK_IDX` (`TSE_EXC_ID` ASC),
  INDEX `TSE_TSR_FK_IDX` (`TSE_TSR_ID` ASC),
